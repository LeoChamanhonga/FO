// Adiciona um listener de eventos de clique ao item do menu "Obras"
document.getElementById('obras').addEventListener('click', function(event) {
    // Impede o comportamento padrão do link
    event.preventDefault();
    // Seleciona o submenu correspondente
    let submenu = document.getElementById('submenu-obras');
    // Alternar visibilidade do submenu
    submenu.classList.toggle('visible');
});

